function renderPracticeNotes(input: ArticleInput) {
  const primarySource = input.researchSources[0];
  const secondarySource = input.researchSources[1];
  const firstRow = input.tableRows[0];
  const secondRow = input.tableRows[1] ?? input.tableRows[0];
  const firstChecklist = input.checklist[0];
  const secondChecklist = input.checklist[1] ?? input.checklist[0];
  const tagText = input.tags.join(", ");

  return `
<h2>${input.category} 적용 전 실무 메모</h2>
<p>${input.title}을 실제 업무에 적용할 때는 먼저 ${firstRow[0]} 항목부터 확인하는 편이 좋습니다. 여기서 볼 내용은 ${firstRow[1]}이고, 판단 기준은 ${firstRow[2]}입니다. 이 첫 기준이 정해져야 이후 논의가 감정이나 취향으로 흐르지 않습니다. 창업 초기에는 자료가 부족하기 때문에 더더욱 확인 가능한 근거와 담당자가 필요합니다.</p>
<p>두 번째로 봐야 할 항목은 ${secondRow[0]}입니다. 이 항목은 ${secondRow[1]}을 확인하는 데서 끝나지 않고 ${secondRow[2]} 기준으로 다음 행동을 정해야 합니다. 예를 들어 고객에게 고지할 문구를 바꿀지, 내부 검토를 받을지, 결제 화면을 수정할지, 계약서에 조항을 넣을지처럼 실행 단위로 바꿔야 합니다.</p>
<p>공식 확인처는 최소 두 곳을 함께 봅니다. 이번 글에서는 ${primarySource.publisher}의 ${primarySource.title} 자료와 ${secondarySource.publisher}의 ${secondarySource.title} 자료를 우선 확인 대상으로 삼았습니다. 공식 사이트도 고시, 안내문, 메뉴 구조가 바뀔 수 있으므로 확인한 날짜를 남겨야 합니다. 그래야 나중에 기준 변경이 있었을 때 어떤 안내를 보고 결정했는지 설명할 수 있습니다.</p>
<p>실무 체크는 ${firstChecklist}에서 시작하면 됩니다. 이어서 ${secondChecklist}까지 확인하면 초기 누락을 줄일 수 있습니다. 이 두 항목은 단순해 보여도 실제 운영에서는 비용, 일정, 고객 신뢰, 내부 책임을 가르는 기준이 됩니다. 특히 돈이 오가거나 고객 권리에 영향을 주는 업무는 화면 문구와 내부 정책이 반드시 같아야 합니다.</p>
<p>${tagText}와 관련된 업무는 단독으로 끝나지 않습니다. 재무, 마케팅, 고객지원, 제품, 법무, 노무 중 하나와 반드시 연결됩니다. 그래서 한 사람이 기억하는 방식으로 처리하면 시간이 지나면서 기준이 흐려집니다. 짧은 문서라도 파일 위치와 담당자를 정하면 팀이 같은 기준을 공유할 수 있습니다.</p>
<p>마지막으로 ${input.category} 업무는 한 번 정리하고 끝내지 말고 월말이나 캠페인 종료 시점에 다시 열어봐야 합니다. 실제 고객 문의와 내부 예외가 생기면 문서를 수정하세요. 이 수정 이력이 쌓이면 외부 자료보다 내 사업에 맞는 운영 매뉴얼이 됩니다.</p>
  `.trim();
}

function renderFaqs(input: ArticleInput) {
  const topic = input.category;
  const faqs: Array<[string, string]> = [
    [
      `${topic} 기준은 언제부터 만들어야 하나요?`,
      "처음 고객, 직원, 거래처, 결제가 생기는 시점부터 필요합니다. 작게 시작해도 기준을 남기면 이후 수정과 설명 비용이 줄어듭니다.",
    ],
    [
      "공식 자료만 보면 충분한가요?",
      "공식 자료는 기본 기준입니다. 다만 내 업종과 계약 구조에 따라 적용 방식이 달라질 수 있으므로 중요한 결정은 전문가 검토로 보완하는 편이 안전합니다.",
    ],
    [
      "문서를 매번 업데이트해야 하나요?",
      "모든 문서를 매주 바꿀 필요는 없습니다. 고객 문의, 결제 분쟁, 신고 일정, 계약 변경처럼 실제 예외가 생겼을 때만 짧게 업데이트해도 충분합니다.",
    ],
    [
      "가장 먼저 정리할 항목은 무엇인가요?",
      "고객이나 외부 기관에 설명해야 하는 항목부터 정리하세요. 가격, 환불, 일정, 책임, 증빙, 담당자처럼 질문을 받았을 때 바로 답해야 하는 것들이 우선입니다.",
    ],
  ];

  return faqs
    .map(
      ([question, answer]) => `<h3>Q. ${question}</h3>
<p>${answer}</p>`,
    )
    .join("\n");
}
