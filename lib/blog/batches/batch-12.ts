function createPost(input: ArticleInput): BlogPost {
  return {
    slug: input.slug,
    title: input.title,
    description: input.description,
    publishedAt: input.publishedAt,
    category: input.category,
    tags: input.tags,
    qualityScore: input.qualityScore,
    researchSources: input.researchSources,
    content: renderArticle(input),
  };
}

function section(heading: string, paragraphs: string[]) {
  return { heading, paragraphs };
}

function officialSources(
  sources: Array<[string, string, string]>,
): BlogResearchSource[] {
  return sources.map(([title, url, publisher]) => ({
    title,
    url,
    publisher,
    checkedAt: "2026-05-16",
  }));
}

function renderArticle(input: ArticleInput) {
  const summary = [
    `${input.category} 업무는 시작 전 기준, 진행 중 기록, 종료 후 점검을 나누어 관리해야 합니다.`,
    `공식 확인처는 ${input.researchSources[0].publisher}, ${input.researchSources[1].publisher}, ${input.researchSources[2].publisher} 자료를 우선 봅니다.`,
    "결정이 필요한 항목은 말로 남기지 말고 날짜, 담당자, 근거가 있는 문서로 보관합니다.",
  ];

  return `
<p>${input.problem}</p>

<div class="tf-tldr">
<p class="tf-tldr-title">핵심 요약</p>
<ul>
${summary.map((item) => `<li>${item}</li>`).join("\n")}
</ul>
</div>

<h2>${input.title} 한눈에 보기</h2>
<table>
<thead><tr><th>구분</th><th>먼저 볼 내용</th><th>확인 기준</th></tr></thead>
<tbody>
${input.tableRows.map(([a, b, c]) => `<tr><td>${a}</td><td>${b}</td><td>${c}</td></tr>`).join("\n")}
</tbody>
</table>

${input.sections
  .map(
    (section) => `<h2>${section.heading}</h2>
${section.paragraphs.map((paragraph) => `<p>${paragraph}</p>`).join("\n")}`,
  )
  .join("\n\n")}

${renderPracticeNotes(input)}

<h2>실행 전 체크리스트</h2>
<ul>
${input.checklist.map((item) => `<li>${item}</li>`).join("\n")}
</ul>

<h2>자주 묻는 질문</h2>
${renderFaqs(input)}

<h2>마지막 판단 기준</h2>
<p>${input.closing}</p>
<p>창업자는 모든 일을 완벽하게 준비할 수는 없습니다. 하지만 지금 어떤 기준이 필요한지, 어떤 증빙이 없는지, 누구에게 확인해야 하는지는 정리할 수 있습니다. 이 정리가 되어 있으면 상담을 받아도 답이 빨라지고 지원사업을 신청해도 수정 시간이 줄어듭니다.</p>
<p>실행은 작게 시작하는 편이 좋습니다. 오늘은 관련 공식 사이트 3곳을 저장하고, 내 상황에 맞는 체크 항목을 표로 옮겨보세요. 그다음 부족한 자료를 하나씩 채우면 됩니다. 창업 운영은 큰 결심보다 작은 기준을 반복해서 지키는 과정입니다.</p>
<p>마지막으로 이 글은 방향을 잡기 위한 안내입니다. 현금, 세무, 법률, 안전, 개인정보처럼 책임이 큰 주제는 공식기관 안내와 전문가 검토를 함께 확인해야 합니다. 특히 계약서 서명, 지원금 집행, 직원 채용, 개인정보 수집, 투자계약 체결 전에는 최신 기준을 다시 확인하세요.</p>
<p>좋은 창업자는 모든 답을 외운 사람이 아니라 확인해야 할 질문을 아는 사람입니다. 질문 목록이 생기면 다음 행동은 훨씬 선명해집니다.</p>
  `.trim();
}

function renderPracticeNotes(input: ArticleInput) {
  const primarySource = input.researchSources[0];
  const secondarySource = input.researchSources[1];
  const firstRow = input.tableRows[0];
  const secondRow = input.tableRows[1] ?? input.tableRows[0];
  const firstChecklist = input.checklist[0];
  const secondChecklist = input.checklist[1] ?? input.checklist[0];
  const tagText = input.tags.join(", ");

  return `
<h2>${input.category} 실행 전 현장 점검 메모</h2>
<p>${input.title}을 실제로 적용할 때는 먼저 ${firstRow[0]} 항목부터 확인하는 편이 좋습니다. 이 단계에서 보는 내용은 ${firstRow[1]}이고, 판단 기준은 ${firstRow[2]}입니다. 많은 창업자가 자료를 한 번에 정리하려고 하지만 실제 업무에서는 기준을 먼저 세우고 그 기준에 맞는 증빙을 채우는 순서가 더 안정적입니다. 특히 ${input.category} 영역은 기관, 계약 상대, 세무와 법무 담당자가 각자 다른 관점으로 같은 자료를 보기 때문에 처음부터 파일명, 날짜, 금액, 담당자를 맞춰 두는 것이 중요합니다.</p>
<p>두 번째로 봐야 할 부분은 ${secondRow[0]}입니다. 여기의 핵심은 ${secondRow[1]}에 그치지 않고, ${secondRow[2]} 기준으로 다음 행동을 정하는 것입니다. 예를 들어 공고 신청, 계약 검토, 직원 채용, 투자자 미팅, 정산 보고처럼 외부 확인을 받는 업무는 말로 설명한 내용보다 문서로 남긴 내용이 우선됩니다. 회의에서 정한 내용은 회의록, 견적서, 계약서 초안, 화면 캡처, 이메일처럼 다시 확인 가능한 형태로 보관해야 합니다.</p>
<p>공식 확인처는 최소 두 곳을 같이 보는 것이 안전합니다. 이번 글에서는 ${primarySource.publisher}의 ${primarySource.title} 자료와 ${secondarySource.publisher}의 ${secondarySource.title} 자료를 우선 확인 대상으로 잡았습니다. 공식 사이트의 메뉴 구조와 공고명은 바뀔 수 있으므로 링크만 저장하지 말고, 확인한 날짜와 해당 부서명도 함께 남겨두세요. 나중에 기준이 바뀌었을 때 어느 시점의 안내를 보고 의사결정했는지 설명할 수 있어야 합니다.</p>
<p>실무 체크는 ${firstChecklist}에서 시작하면 됩니다. 이어서 ${secondChecklist}까지 확인하면 최소한의 누락을 줄일 수 있습니다. 이 두 항목을 정리하지 않은 상태에서 비용을 쓰거나 계약을 맺으면 이후 수정 비용이 커집니다. 반대로 초기 기준표를 만들고 관련 파일을 같은 폴더에 모아두면, 지원사업 신청, 내부 보고, 전문가 상담, 세무 검토 같은 자료를 재사용할 수 있습니다.</p>
<p>${tagText}와 관련된 업무는 겉으로 보기에는 행정 절차처럼 보여도 실제로는 사업의 신뢰도를 만드는 과정입니다. 창업 초기에는 속도가 중요하지만, 속도만 앞서면 나중에 설명할 수 없는 결정이 쌓입니다. 금액, 일정, 책임 범위, 산출물, 권리 귀속, 개인정보 처리, 성과 지표처럼 나중에 문제가 되는 항목은 짧은 문장이라도 기준을 남겨두세요. 그 기준이 있어야 담당자가 바뀌거나 시간이 지나도 같은 판단을 유지할 수 있습니다.</p>
<p>마지막으로 ${input.category} 업무는 한 번 정리하고 끝나는 성격이 아닙니다. 월말이나 캠페인 종료 전에 ${firstRow[0]}와 ${secondRow[0]} 항목을 다시 열어 실제 결과가 처음 세운 기준과 맞았는지 비교해야 합니다. 기준이 맞지 않았다면 실패로 넘기지 말고 다음 발주, 다음 계약, 다음 광고, 다음 고객 응대에서 바꿀 문장 하나를 정하세요. 이렇게 수정 이력이 쌓이면 작은 사업도 운영 매뉴얼을 갖게 됩니다.</p>
  `.trim();
}

function renderFaqs(input: ArticleInput) {
  const topic = input.category;
  const faqs: Array<[string, string]> = [
    [
      `${topic}는 창업 초기에 꼭 챙겨야 하나요?`,
      "네. 초기 기준을 잡아두면 나중의 비용과 리스크가 줄어듭니다. 작게 시작하더라도 기록과 기준은 먼저 만드는 편이 좋습니다.",
    ],
    [
      "전문가에게 바로 맡기면 대표가 몰라도 되나요?",
      "아닙니다. 전문가 검토는 필요할 수 있지만 대표가 기본 구조를 알아야 의사결정이 가능합니다. 맡기더라도 기준과 책임은 대표에게 남습니다.",
    ],
    [
      "정부 지원사업과도 관련이 있나요?",
      "관련이 큽니다. 지원사업은 신청, 협약, 집행, 정산에서 증빙과 기준을 계속 확인합니다. 평소 자료가 정리되어 있으면 신청과 사후관리가 모두 쉬워집니다.",
    ],
    [
      "가장 먼저 할 일은 무엇인가요?",
      "현재 상태를 표로 정리하는 것입니다. 해당자, 기준, 증빙, 마감일을 한 줄씩 적으면 바로 다음 행동이 보입니다.",
    ],
  ];

  return faqs
    .map(
      ([question, answer]) => `<h3>Q. ${question}</h3>
<p>${answer}</p>`,
    )
    .join("\n");
}
