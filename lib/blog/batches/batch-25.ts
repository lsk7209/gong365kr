function createPost(input: ArticleInput): BlogPost {
  return {
    slug: input.slug,
    title: input.title,
    description: input.description,
    publishedAt: input.publishedAt,
    category: input.category,
    tags: input.tags,
    content: renderArticle(input),
    researchSources: input.researchSources,
    qualityScore: input.qualityScore,
  };
}

function officialSources(
  rows: Array<[string, string, string]>,
): BlogResearchSource[] {
  return rows.map(([title, url, publisher]) => ({
    title,
    url,
    publisher,
    checkedAt: "2026-05-16",
  }));
}

function section(heading: string, paragraphs: string[]) {
  return { heading, paragraphs };
}

function renderArticle(input: ArticleInput): string {
  const tableRows = input.tableRows
    .map(([area, check, reason]) => `<tr><td>${area}</td><td>${check}</td><td>${reason}</td></tr>`)
    .join("");
  const sections = input.sections
    .map(
      (item) => `<h2>${item.heading}</h2>${item.paragraphs
        .map((paragraph) => `<p>${paragraph}</p>`)
        .join("")}`,
    )
    .join("");

  return `
<article>
  <div class="tf-tldr">
    <strong>핵심 요약</strong>
    <p>${input.problem}</p>
  </div>
  <p>${input.problem}</p>
  <table>
    <thead><tr><th>영역</th><th>확인할 것</th><th>운영 이유</th></tr></thead>
    <tbody>${tableRows}</tbody>
  </table>
  ${sections}
  ${renderPracticeNotes(input)}
  ${renderFaqs(input)}
  <h2>마무리</h2>
  <p>${input.closing}</p>
</article>`.trim();
}

