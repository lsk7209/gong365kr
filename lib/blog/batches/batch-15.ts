function createPost(input: ArticleInput): BlogPost {
  return {
    slug: input.slug,
    title: input.title,
    description: input.description,
    publishedAt: input.publishedAt,
    category: input.category,
    tags: input.tags,
    qualityScore: input.qualityScore,
    researchSources: input.researchSources,
    content: renderArticle(input),
  };
}

function section(heading: string, paragraphs: string[]) {
  return { heading, paragraphs };
}

function officialSources(
  sources: Array<[string, string, string]>,
): BlogResearchSource[] {
  return sources.map(([title, url, publisher]) => ({
    title,
    url,
    publisher,
    checkedAt: "2026-05-16",
  }));
}

