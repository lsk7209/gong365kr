function renderPracticeNotes(input: ArticleInput) {
  const primarySource = input.researchSources[0];
  const secondarySource = input.researchSources[1];
  const firstRow = input.tableRows[0];
  const secondRow = input.tableRows[1] ?? input.tableRows[0];
  const firstChecklist = input.checklist[0];
  const secondChecklist = input.checklist[1] ?? input.checklist[0];
  const tagText = input.tags.join(", ");

  return `
<h2>${input.category} 실무 적용 메모</h2>
<p>${input.title}을 현장에 적용할 때는 먼저 ${firstRow[0]} 항목을 기준으로 잡는 것이 좋습니다. 여기서 확인할 내용은 ${firstRow[1]}이고, 판단 기준은 ${firstRow[2]}입니다. 이 기준이 먼저 있어야 회의에서 나온 의견과 실제 실행 항목을 구분할 수 있습니다. 기준 없이 시작하면 담당자마다 다르게 해석하고, 나중에는 왜 그렇게 결정했는지 설명하기 어려워집니다.</p>
<p>다음으로 ${secondRow[0]} 항목을 봅니다. 이 항목의 핵심은 ${secondRow[1]}을 단순히 적는 데서 끝나지 않고 ${secondRow[2]} 기준으로 다음 행동을 정하는 것입니다. 창업 초기에는 모든 자료가 부족하기 때문에 완벽한 답보다 확인 가능한 근거가 더 중요합니다. 계약서, 공고문, 견적서, 회의록, 고객 문의, 화면 캡처처럼 다시 열어볼 수 있는 자료를 함께 묶어두세요.</p>
<p>공식 확인처는 최소 두 곳 이상을 함께 보는 편이 안전합니다. 이번 글에서는 ${primarySource.publisher}의 ${primarySource.title} 자료와 ${secondarySource.publisher}의 ${secondarySource.title} 자료를 우선 확인 대상으로 잡았습니다. 공식 사이트도 메뉴 이름과 세부 안내가 바뀔 수 있으므로 확인한 날짜를 남겨야 합니다. 나중에 기준이 바뀌었을 때 어느 시점의 안내를 보고 의사결정했는지 설명할 수 있어야 합니다.</p>
<p>실무 체크는 ${firstChecklist}에서 시작하면 됩니다. 이어서 ${secondChecklist}까지 확인하면 초기 누락을 상당히 줄일 수 있습니다. 이 두 항목은 작아 보이지만 실제 운영에서는 비용, 일정, 고객 신뢰, 내부 책임을 가르는 기준이 됩니다. 특히 외부 기관, 거래처, 고객, 직원과 연결되는 업무는 말보다 문서가 우선합니다.</p>
<p>${tagText}와 관련된 업무는 단독으로 움직이지 않습니다. 재무, 마케팅, 고객지원, 제품, 법무, 노무 중 하나와 반드시 연결됩니다. 그래서 한 부서나 한 사람이 모든 것을 기억하는 방식은 오래가지 않습니다. 짧은 기준표를 만들고 관련 파일을 같은 폴더에 넣어두면 담당자가 바뀌어도 같은 판단을 이어갈 수 있습니다.</p>
<p>마지막으로 ${input.category} 업무는 월말이나 캠페인 종료 시점에 다시 열어봐야 합니다. 처음 정한 기준이 실제로 맞았는지, 예외가 얼마나 생겼는지, 다음에는 어떤 문장을 고쳐야 하는지 기록하세요. 작은 수정 이력이 쌓이면 별도의 컨설팅 문서보다 실용적인 내부 매뉴얼이 됩니다. 이 과정이 반복되어야 창업자의 감각이 팀의 운영 기준으로 바뀝니다.</p>
  `.trim();
}

