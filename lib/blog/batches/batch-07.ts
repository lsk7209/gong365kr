function renderArticle(input: ArticleInput) {
  return `
<p>${input.intro}</p>

<div class="tf-tldr">
<p class="tf-tldr-title">핵심 요약</p>
<ul>
${input.summary.map((item) => `<li>${item}</li>`).join("\n")}
</ul>
</div>

<h2>${input.title} 한눈에 보기</h2>
<table>
<thead><tr><th>구분</th><th>먼저 볼 내용</th><th>확인 기준</th></tr></thead>
<tbody>
${input.tableRows.map(([a, b, c]) => `<tr><td>${a}</td><td>${b}</td><td>${c}</td></tr>`).join("\n")}
</tbody>
</table>

${input.sections
  .map(
    (section) => `<h2>${section.heading}</h2>
${section.paragraphs.map((paragraph) => `<p>${paragraph}</p>`).join("\n")}`,
  )
  .join("\n\n")}

${renderPracticeNotes(input)}

<h2>실행 전 체크리스트</h2>
<ul>
${input.checklist.map((item) => `<li>${item}</li>`).join("\n")}
</ul>

<h2>자주 묻는 질문</h2>
${input.faqs
  .map(
    ([question, answer]) => `<h3>Q. ${question}</h3>
<p>${answer}</p>`,
  )
  .join("\n")}

<h2>마지막 판단 기준</h2>
<p>${input.closing}</p>
<p>창업자는 모든 일을 완벽히 알 수는 없습니다. 하지만 지금 어떤 기준이 필요한지, 어떤 증빙이 없는지, 누구에게 확인해야 하는지는 정리할 수 있습니다. 이 정리가 되어 있으면 상담을 받아도 답이 빨라지고 지원사업을 신청해도 수정 시간이 줄어듭니다.</p>
<p>실행은 작게 시작하는 편이 좋습니다. 오늘은 관련 공식 사이트 3곳을 저장하고 내 상황에 맞는 체크 항목을 표로 옮겨보세요. 그다음 부족한 자료를 하나씩 채우면 됩니다. 창업 운영은 큰 결심보다 작은 기준을 반복해서 지키는 과정입니다.</p>
<p>마지막으로, 이 글은 방향을 잡기 위한 안내입니다. 세금, 노무, 법률, 투자, 개인정보처럼 책임이 큰 주제는 공식기관 안내와 전문가 검토를 함께 확인해야 합니다. 특히 계약서 서명, 지원금 집행, 직원 채용, 개인정보 수집, 투자계약 체결 전에는 최신 기준을 다시 확인하세요.</p>
<p>좋은 창업자는 모든 답을 외운 사람이 아니라 확인해야 할 질문을 아는 사람입니다. 질문 목록이 생기면 다음 행동은 훨씬 선명해집니다.</p>
  `.trim();
}

function renderPracticeNotes(input: ArticleInput) {
  const primarySource = input.researchSources[0];
  const secondarySource = input.researchSources[1];
  const firstRow = input.tableRows[0];
  const secondRow = input.tableRows[1] ?? input.tableRows[0];
  const firstChecklist = input.checklist[0];
  const secondChecklist = input.checklist[1] ?? input.checklist[0];
  const tagText = input.tags.join(", ");

  return `
<h2>${input.category} 실행 전 현장 점검 메모</h2>
<p>${input.title}을 실제로 적용할 때는 먼저 ${firstRow[0]} 항목부터 확인하는 편이 좋습니다. 이 단계에서 보는 내용은 ${firstRow[1]}이고, 판단 기준은 ${firstRow[2]}입니다. 많은 창업자가 자료를 한 번에 정리하려고 하지만, 실제 업무에서는 기준을 먼저 세우고 그 기준에 맞는 증빙을 채우는 순서가 더 안정적입니다. 특히 ${input.category} 영역은 담당 기관, 계약 상대, 세무·노무·법무 전문가가 각자 다른 관점으로 같은 자료를 보기 때문에 처음부터 파일명, 날짜, 금액, 담당자를 맞춰 두는 것이 중요합니다.</p>
<p>두 번째로 봐야 할 부분은 ${secondRow[0]}입니다. 여기서 핵심은 ${secondRow[1]}을 단순히 적어두는 데 그치지 않고, ${secondRow[2]} 기준으로 다음 행동을 정하는 것입니다. 예를 들어 공고 신청, 계약 검토, 직원 채용, 투자 미팅, 정산 보고처럼 외부 확인을 받는 업무는 말로 설명한 내용보다 문서로 남긴 내용이 우선합니다. 내부 회의에서 정한 내용도 회의록, 견적서, 계약서 초안, 화면 캡처, 담당자 메일처럼 다시 확인 가능한 형태로 남겨야 합니다.</p>
<p>공식 확인처는 최소 두 곳을 같이 보는 것이 안전합니다. 이번 글에서는 ${primarySource.publisher}의 ${primarySource.title} 자료와 ${secondarySource.publisher}의 ${secondarySource.title} 자료를 우선 확인 대상으로 잡았습니다. 공식 사이트의 메뉴 구조나 공고명은 바뀔 수 있으므로 링크만 저장하지 말고, 확인한 날짜와 담당 부서명도 같이 남겨야 합니다. 나중에 기준이 바뀌었을 때 어느 시점의 안내를 보고 의사결정했는지 설명할 수 있어야 합니다.</p>
<p>실무 체크는 ${firstChecklist}에서 시작하면 됩니다. 이어서 ${secondChecklist}까지 확인하면 최소한의 누락을 줄일 수 있습니다. 이 두 항목이 정리되지 않은 상태에서 비용을 쓰거나 계약을 맺으면 이후 수정 비용이 커집니다. 반대로 초기에 기준표를 만들고 관련 파일을 같은 폴더에 모아두면, 지원사업 신청서, 내부 보고, 전문가 상담, 외부 파트너 협의에 같은 자료를 재사용할 수 있습니다.</p>
<p>${tagText}와 관련된 업무는 겉으로 보기에는 행정 절차처럼 보여도 실제로는 사업의 신뢰도를 만드는 과정입니다. 창업 초기에는 속도가 중요하지만, 속도만 앞서면 나중에 설명할 수 없는 결정이 쌓입니다. 금액, 일정, 책임 범위, 산출물, 권리 귀속, 개인정보 처리, 성과 지표처럼 나중에 문제가 될 수 있는 항목은 짧은 문장이라도 기준을 써두세요. 이 기준이 있어야 담당자가 바뀌거나 시간이 지나도 같은 판단을 유지할 수 있습니다.</p>
  `.trim();
}
